//! Документация для системы управления базой данных NetBit
//! 
//! Этот модуль содержит документацию для работы с SQLite базой данных,
//! включая подключение, миграции, и основные операции с данными.

// Импорты из реальной реализации
pub use crate::core::database::Database;

/// Основной класс для работы с базой данных NetBit
///
/// Предоставляет единый интерфейс для всех операций с SQLite базой данных.
/// Автоматически создает таблицы при инициализации и поддерживает миграции.
///
/// # Использование
/// ```rust,no_run
/// use netbit_backend::core::database_doc::DocumentedDatabase;
/// 
/// // Создание подключения к базе данных
/// let database = DocumentedDatabase::new("gitea.db")?;
/// 
/// // Тестирование подключения
/// database.test_connection()?;
/// 
/// // Выполнение миграций
/// database.migrate()?;
/// 
/// // Получение подключения для операций
/// let conn = database.get_connection();
/// ```
///
/// # Основные методы
/// - `new(path)` - создание подключения
/// - `test_connection()` - проверка работоспособности
/// - `migrate()` - выполнение миграций
/// - `get_connection()` - получение thread-safe подключения
/// - `clone()` - клонирование ссылки на подключение
pub use Database as DocumentedDatabase;